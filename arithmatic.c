#include <stdio.h>
int main()
{
    int a = 20;
    int b = 8;
    int sum = a + b;
    int sub = a - b;
    int mul = a * b;
    int div = a / b;
    int mod = a % b;
    printf("Sum: %d\n", sum);
    printf("Substraction: %d\n", sub);
    printf("Multiplication: %d\n", mul);
    printf("Division: %d\n", div);
    printf("Modulus: %d\n", mod);
    return 0;
}